    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        String CNPJ = txtExiCNPJ.getText();
        
        FornecedorDAO fDAO = new FornecedorDAO();
        Fornecedor f = fDAO.getFornecedor(CNPJ);


        if(f == null){
            txtExiCNPJ.setText("");
            txtExiNome.setText("");
            txtExiNomeFantasia.setText("");
           

            JOptionPane.showMessageDialog(this, "Fornecedor n√£o encontrado!");
        }
        else{
            txtExiNome.setText(f.getNome());
            txtExiNomeFantasia.setText(f.getNomeFantasia());

        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

